namespace SharpEngine.Core.Audio;

/// <summary>
///     Contains all the supported audio file extensions.
/// </summary>
public static class AudioFileExtensions
{
    /// <summary>The file extension of the Waveform audio file.</summary>
    public const string WavExtension = ".wav";

    /// <summary>The file extension of the MPEG-1 Audio Layer III or MPEG-2 Audio Layer III (MP3) file.</summary>
    public const string Mp3Extension = ".mp3";

    /// <summary>The file extension of the MPEG-4 (MP4) file.</summary>
    public const string Mp4Extension = ".mp4";
}
