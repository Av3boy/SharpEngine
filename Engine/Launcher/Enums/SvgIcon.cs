namespace Launcher.Enums;

/// <summary>
///     Contains the supported SVG icons.
/// </summary>
public enum SvgIcon
{
    /// <summary>Indicates no icon.</summary>
    None,

    /// <summary>Indicates the home icon.</summary>
    Home,

    /// <summary>Indicates the settings icon.</summary>
    Cog,

    /// <summary>Indicates the plus icon.</summary>
    Discard,

    /// <summary>Indicates the search lens icon.</summary>
    Lens,

    /// <summary>Indicates the close 'x' icon.</summary>
    Close,

    /// <summary>Indicates the chevron '&lt;' down icon.</summary>
    ChevronDown,

    /// <summary>Indicates the chevron '&lt;' up icon.</summary>
    ChevronUp,
}
